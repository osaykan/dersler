package day02_wrapperclasses;

public class Wrapper01 {

	public static void main(String[] args) {
		
		// int data tipinin max ve min degerlerini bulup ekrana yazalim
		// int in wrapper class ini kullanacagiz Integer yaz sonuna nokta koy ve sec
		System.out.println(Integer.MAX_VALUE); // Bir class in icindeki metodlara ulasmak class isminden sonra nokta koy,listeden secim yap
		System.out.println(Integer.MIN_VALUE);
		
		
		System.out.println(Long.MAX_VALUE);
		System.out.println(Long.MIN_VALUE);
		System.out.println(Short.MAX_VALUE);
		System.out.println(Short.MIN_VALUE);
        System.out.println(Double.MIN_VALUE);		
		
		
		
		
		
		
		
	}

}
