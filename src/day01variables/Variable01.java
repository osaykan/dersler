		char harf = 'S'; // Char lara deger atarken mutlaka tek tirnak arasina yazilir
		
		System.out.println(maas); // Ekranda 3000 görülür
		System.out.println(harf);
		boolean isOlt = true;
		System.out.println(isOlt); //boolean lar icin sadece true veya false degerlerini kullanabiliriz
		
		boolean isNew = false;
		System.out.println(isNew);
		
		byte derinlik = -123;
		System.out.println(derinlik);
		
		 short nazilliNüfus = 28000;
