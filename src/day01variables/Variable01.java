		 float para = 5.25f;//Eger bir ondalik kesri float yapmak istersek sonuna f veya F koruz
		 System.out.println(para);// Eger =5.25 yazmasaydik Default value = 0.0f görünürdü
		 
		 String ögrenciIsmi ="Ali Can";
