package day05operators;

public class Decrement01 {

	public static void main(String[] args) {
		// int data tipinde bir variable olusturun
		//Bunun degerini uc yontemide kullanarak her def. 1 azaltin
		int num1 = 12;
		
		num1 = num1 -1;
		System.out.println(num1);
		
		num1-=1;
		System.out.println(num1);

		num1--;
		System.out.println(num1);
		
		
	}

}
