package day05operators;

public class Increment01 {

	public static void main(String[] args) {
		// int data tipinde iki variable olusturun. Birinciyi 3, ikinciyi 7 artirin
		
		
		int sayi1 = 12;
		sayi1 = sayi1 + 3;          // increment ==> 1.yol
		System.out.println(sayi1);
		

		int sayi2 = 20;
		sayi2 = sayi2 + 7;            // increment ==> 1.yol
		System.out.println(sayi2);
		
		
	}

}
