package day05operators;

public class Increment02 {

	public static void main(String[] args) {
	// Ikitane int data tipinde ikitane variable olustur. Birinciyi 3, ikinciyi 7 artir
		
		
		int sayi1 = 12;
		sayi1+=3;         // + ile = arasina bosluk birakmayin
		System.out.println(sayi1);
		
		int sayi2 = 20;
		sayi2+=7;
		System.out.println(sayi2);
		
		int sayi3 = 20;
		sayi3 = sayi3+7 ;//20+7
		sayi3+=7;// 27+7
		System.out.println(sayi3);

	}

}
