package day05operators;

public class Increment03 {

	public static void main(String[] args) {
		// int data tipinde ikitane variable olusturun
		// Bu variable ri en kisa yolu kullanarak 1 artirin
		//En kisa yil sadece 1 artirmak icin kullanilir
		
		int sayi1 = 12;
		sayi1++;         // ++ yazilirsa sayi 1 artar
		System.out.println(sayi1);

		
		int sayi2 = 20;
		sayi2++;
		System.out.println(sayi2);
		
		
	}

}
